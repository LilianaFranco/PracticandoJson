//Recorrer el JSON e insertar en el listado del html los nombres de los miembros
function recorrerMiembros() {
    const heroesSquad = JSON.parse(jsonData);
    let heroes = heroesSquad.members;
    console.log(heroes);
    for (const iterator in heroes) {
      const li = document.createElement("li");
      const parentList = document.getElementById("miembros")
      parentList.appendChild(li)
      li.innerText = heroes[iterator].name
    }
};
